def find_first_non_representable(M):
    """
    Находит первое натуральное число, не представимое суммой элементов массива M.

    Параметры:
        M - список натуральных чисел, упорядоченный по неубыванию

    Возвращает:
        Первое натуральное число, которое нельзя получить как сумму
        любого подмножества элементов M (каждый элемент используется 0 или 1 раз)
    """
    S = 0  # Текущая граница представимых чисел (от 1 до S)

    for x in M:
        # Если текущий элемент больше, чем S + 1,
        # то число S + 1 нельзя представить
        if x > S + 1:
            return S + 1

        # Иначе расширяем диапазон представимых чисел
        S += x

    # Если все элементы обработаны, первое непредставимое число - S + 1
    return S + 1


# Пример использования
if __name__ == "__main__":
    # Пример из условия (можно заменить на любой другой упорядоченный массив)
    M = [1, 2, 4, 9]

    result = find_first_non_representable(M)
    print(f"Первое непредставимое число: {result}")
