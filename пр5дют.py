# Запрашиваем у пользователя количество членов ряда
k = int(input("Введите количество членов ряда k: "))

# Инициализируем переменные
сумма = 0.0      # Здесь будет храниться итоговая сумма
факториал = 1   # Здесь будем хранить текущее значение факториала

# Вычисляем сумму по шагам
for i in range(1, k + 1):
    факториал *= i          # Вычисляем i! на основе предыдущего факториала
    сумма += 1 / факториал  # Добавляем 1/i! к общей сумме

# Выводим результат
print(f"Сумма ряда S = {сумма}")
